const CHAR_W = 5;
const CHAR_H = 7;

/**
 * テキストを RGBA buffer に描画する。等幅、1 文字ごとに空きセルを 1 列挟む。
 *
 * @param {Buffer} buffer 描画対象 RGBA buffer
 * @param {number} bufW キャンバス幅 (px)
 * @param {number} bufH キャンバス高さ (px)
 * @param {string} text 描画テキスト
 * @param {number} x 左上 x (px)
 * @param {number} y 左上 y (px)
 * @param {number} scale ドットあたりのピクセル数
 * @param {{ r:number, g:number, b:number }} color 前景色
 */
export function drawText(buffer, bufW, bufH, text, x, y, scale, color) {
  const cellW = (CHAR_W + 1) * scale;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    const glyph = GLYPHS[ch] ?? GLYPHS[' '];
    const gx = x + i * cellW;
    for (let row = 0; row < CHAR_H; row++) {
      const rowStr = glyph[row];
      for (let col = 0; col < CHAR_W; col++) {
        if (rowStr[col] !== '1') continue;
        for (let dy = 0; dy < scale; dy++) {
          for (let dx = 0; dx < scale; dx++) {
            const px = gx + col * scale + dx;
            const py = y + row * scale + dy;
            if (px < 0 || px >= bufW || py < 0 || py >= bufH) continue;
            const idx = (py * bufW + px) << 2;
            buffer[idx] = color.r;
            buffer[idx + 1] = color.g;
            buffer[idx + 2] = color.b;
            buffer[idx + 3] = 0xff;
          }
        }
      }
    }
  }
}

/** 描画するテキストのピクセル幅を返す（drawText と同じ余白ルール）。 */
export function measureText(text, scale) {
  const cellW = (CHAR_W + 1) * scale;
  return text.length * cellW - scale;
}

